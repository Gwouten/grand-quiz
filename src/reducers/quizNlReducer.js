const quizNlReducer = (state = quizNl, action) => {
    switch(action.type){

        case 'SET_ANSWERED':
        return [...state].map((question) => {
            if(question.questionNumber === action.number) {
                return {...question, answered: true}
            } else {
                return question;
            }
        });

        case 'SET_STATUS':
        return [...state].map((question) => {
            if(question.questionNumber === action.number) {
                return {...question, correct: true}
            } else {
                return question;
            }
        });

        default:
        return state;
    }
}

export default quizNlReducer